
public class PlaneCoordinate {
	private  double x;
	private double y;
	public void setX(double X){
		this.x=X;
	}
	public void setY(double Y){
		this.y=Y;
	}
	public double getX(){
		return x;
	}
	public double getY(){
		return y;
	}
	public String toString(){
		return "X:"+x+"\t"+"Y:"+y+"\t";
		
	}
}
